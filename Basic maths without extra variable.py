# program to do basic maths without adding extra variable

a = int(input('Enter value of a :'))
b = int(input('Enter value of b :'))

print ('%d + %d = %.1f'%(a,b,a+b))
print ('%d - %d = %.2f'%(a,b,a-b))
print ('%d * %d = %.3f'%(a,b,a*b))
print ('%d / %d = %.4f'%(a,b,a/b))
print ('%d %% %d = %.5f'%(a,b,a%b))
print ('%d ** %d = %.6f'%(a,b,a**b))
